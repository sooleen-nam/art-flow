function getLang()       { return localStorage.getItem('artflow_lang') || 'ko'; }
function saveLang(lang)  { localStorage.setItem('artflow_lang', lang); }
function t(key, ...args) {
  const lang = getLang();
  const dict = I18N[lang] || I18N.ko;
  const val  = dict[key] !== undefined ? dict[key] : (I18N.ko[key] !== undefined ? I18N.ko[key] : key);
  return typeof val === 'function' ? val(...args) : val;
}
